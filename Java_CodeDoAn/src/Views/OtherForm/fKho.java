    public void loadData(){
        panel.removeAll();
        panel.setLayout(new WrapLayout(WrapLayout.CENTER, 20, 20));
        jScrollPane1.setVerticalScrollBar(new ScrollBar());
        List<KhoXe> list = KhoXeService.getInstance().danhSachKhoXe();
        for (KhoXe xe : list){
            Card_KhoXe khoXe = new Card_KhoXe();
            khoXe.setData(xe);
            panel.add(khoXe);
            panel.revalidate();
            panel.repaint();
        }
    }
    
